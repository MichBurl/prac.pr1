import math

def main():
    # Natural logarithm of 5
    natural_log_5 = math.log(5)
    print("Natural logarithm of 5:", natural_log_5)

    # e raised to the power of 3
    e_power_3 = math.exp(3)
    print("e raised to the power of 3:", e_power_3)

    # Square root of 7
    sqrt_7 = math.sqrt(7)
    print("Square root of 7:", sqrt_7)

    # Sine of 90 degrees (converted to radians)
    sine_90_degrees = math.sin(math.radians(90))
    print("Sine of 90 degrees:", sine_90_degrees)

main()